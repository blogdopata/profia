		<div class="col-md-10 mt-5 mb-5 legal">
        <br>
            <h6 class="titulo-generico">Título II de la Matrícula</h6> 
            <b class="titulo-generico">Capítulo I - Generalidades</b>
            <br><br>
            <b>Artículo 13</b>.- Naturaleza de la matrícula
            <p class="text-fix-style">
            La matrícula es un acto formal y voluntario que acredita la condición de estudiante universitario de la facultad correspondiente y de la universidad.
            </p>
            
            
            
            <br>

            <b>Artículo 14</b>.-  Duración de la matrícula
            <p class="text-fix-style">
            La matrícula tiene validez durante todo el proceso lectivo correspondiente, ya sea para todo el semestre o año académico según la modalidad en cual se matriculó el alumno. 
            

            <br><br>
            Concordancias: Artículos 114º y 115º del Estatuto Universitario
            </p>
            <br>

            <b>Artículo 15</b>.- Efectos de la matrícula
            <br>
            El alumno al matricularse en la universidad está sujeto a los siguientes efectos: 
            <br>

            <div class="listafia">
                <ul class="ml-5">
                    <li>Mediante la matrícula, el alumno manifiesta su acatamiento a las normas establecidas por la autoridad competente, por el Estatuto de la Universidad y por los reglamentos internos aprobados por Consejo Universitario; se compromete a respetarlos y, a través del estudio y de la participación académica, acepta la responsabilidad de velar por el prestigio de la universidad.</li>
                    <li>En la matrícula no se toman en cuenta los cursos suprimidos por cambio de currículo.</li>
                    <li>Los alumnos no podrán matricularse en una asignatura teniendo desaprobada la asignatura prerrequisito, bajo sanción de nulidad, sin opción a rectificación de la matrícula.</li>
                    <li>Los alumnos no matriculados en el curso determinado no tienen derecho a evaluación ni nota.</li>
                    <li>El alumno que solicite el retiro de uno o más cursos, lo podrá hacer hasta la fecha de inicio de exámenes parciales. Este retiro del curso, no generará disminución alguna en el pago de la pensión establecida.</li>
                    <li>Si el alumno se matriculó en cursos que desaprobó en el semestre anterior, no podrá solicitar el retiro de tales cursos.</li>
                </ul>
            </div>
            <br><br>
            <b>Artículo 16</b>.- De la individualidad de la matrícula
            <p class="text-fix-style">
            El alumno debe efectuar su matrícula personalmente, salvo motivos de fuerza mayor, para lo cual designará a una persona autorizada y acreditada mediante una carta poder simple, de presentarse este caso, la facultad no se hará responsable de posibles errores y/o reclamos con respecto a los actos derivados de la matrícula.
            </p>
            <br><br>
            <b>Artículo 17</b>.- Actos administrativos derivados de la matrícula <br><br>
            Son actos administrativos derivados de la matrícula, los siguientes: 
            <br>
            <br>
            <div class="listafia">
                <ul class="ml-5">
                    <li>Matrícula de alumnos ingresantes a través del concurso ordinario de admisión</li>
                    <li>Matrícula de alumnos ingresantes exonerados del concurso ordinario de admisión y convenios</li>
                    <li>Matrícula de alumnos regulares</li>
                    <li>Matrícula de alumnos especiales</li>
                    <li>Retiro del ciclo y/o curso</li>
                    <li>Reserva de matrícula</li>
                    <li>Reactualización de matrícula</li>
                </ul>
            </div>
            <br>
            Concordancias: Artículo 56º de la Ley Universitaria Nro. 23733 <br>
            Artículo 15º y 116º del Estatuto Universitario
          

            <br><br>
            <b>Artículo 18</b>.- De la Ficha de Matrícula 
            <div class="text-fix-style">
            La Ficha de Matrícula es el documento que acredita el derecho del alumno a asistir a clases y ser evaluado en los cursos correspondientes, así como a acceder a los servicios que ofrece la universidad.
            </div>

            <br><br>

            <b>Artículo 19</b>.- Del formato de la Ficha de Matrícula <br>
            En el formato de la Ficha de Matrícula se especifican los siguientes datos: <br>
            <br>

            <div class="listafia">
                <ul>
                    <li>Del alumno : Apellidos y nombres</li>
                    <div class="listaperfil2">
                        <ul class="ml-5">
                            <li>Código</li>
                            <li>Facultad y/o escuela </li>
                            <li>Ciclo de referencia</li>
                            <li>Total de créditos matriculados</li>
                        </ul>
                    </div>
                    <br>
                    <li>De la matrícula : Nombre de la asignatura</li>
                    <div class="listaperfil2">
                        <ul class="ml-5">
                            <li>Código de la asignatura</li>
                            <li>Créditos de la asignatura</li>
                            <li>Ciclo</li>
                            <li>Turno</li>
                            <li>Sección</li>
                            <li>Semestre Académico</li>
                        </ul>
                    </div>
                </ul>
            </div>
            <br><br>
            <b>Artículo 20</b>.- Error en la Ficha de Matrícula
            <p class="text-fix-style">
            De existir algún error en la Ficha de Matrícula, el alumno debe comunicar este hecho de inmediato al personal autorizado de la Oficina de Registros Académicos, a fin de que el error sea corregido a la brevedad posible. 
            <br><br>
            Si el alumno no se percató del error de manera inmediata deberá presentar una solicitud de rectificación ante el Jefe de la Oficina de Registros Académicos, tiene hasta cinco (05) días después del inicio de clases para realizar este trámite y el pago correspondiente.
            </p>
            <br><br>

            <b>Artículo 21</b>.- Pérdida del derecho de matrícula
            El alumno pierde el derecho de matrícula y es separado de la facultad y de la universidad, cuando: 
            <br><br>
            <div class="listafia">
                <ul class="ml-5">
                    <li>El alumno ha desaprobado tres (03) veces la misma asignatura consecutivamente</li>
                    <li>El alumno ha desaprobado dos (02) veces consecutivas tres (03) asignaturas</li>
                    <li>Por razones o causas justificadas que determine el Consejo Universitario</li>
                </ul>
            </div>
            <br>
            <p class="text-fix-style">
            En los casos señalados en los literales a) y b), excepcionalmente y por única vez, el alumno podrá presentar una solicitud de excepción dirigida al Decano de la Facultad, quien a su vez deberá presentar dicha solicitud en Consejo de Facultad para su respectiva evaluación. De ser aprobada por el Consejo de Facultad, se le otorga al alumno matrícula condicional, caso contrario, se le deniega la solicitud. 
            <br><br>
            Concordancias: Artículo 24º del Estatuto Universitario
            </p>





           


           

