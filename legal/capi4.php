            <b>Artículo 58</b>.- Naturaleza
            <p class="text-fix-style">
            El Curso de Nivelación es un proceso especial e intensivo de cuatro semanas de duración, programado para los meses de enero y febrero, en el cual el alumno regular puede matricularse para nivelar o adelantar asignaturas; siempre y cuando se cumplan los pre-requisitos exigidos por cada asignatura. En el caso de los alumnos repitentes, pueden acogerse al Curso de Nivelación, sólo para los cursos desaprobados
            <br>
            Excepcionalmente, en la Facultad de Medicina Humana la duración de los Cursos de Nivelación tendrá un mínimo de duración de seis (06) y un máximo de ocho (08) semanas.
            <br>
            </p>

            <br><br>

            <b>Artículo 59</b>.- De la proyección y programación de los Cursos de Nivelación
            
            <p class="text-fix-style">
            Es responsabilidad del Departamento Académico de cada Facultad elaborar las proyecciones estimadas de los alumnos a matricularse en los Cursos de Nivelación; además deberá programar las asignaturas y horarios, designar a los docentes encargados del Curso de Nivelación.
            </p>
            

            <br><br>

            <b>Artículo 60</b>.- Requisitos para la matrícula en el Curso de Nivelación
            <p class="text-fix-style">
            Los alumnos que soliciten matricularse en al asignaturas del Curso de Nivelación, deben cumplir los siguientes requisitos:
            </p>

            <div class="listafia">
                <ul class="ml-5 mt-2 mb-2">
                <li>Constancia de Tesorería (no tener deudas pendientes con la Universidad)</li>
                <li>Matricularse en un máximo de doce (12) créditos.</li>
                <li>No matricularse en más de una asignatura por turno, salvo que no haya cruce de horarios, que es el caso de las asignaturas de dos (2) y tres (3) créditos.</li>
                <li>Recibo cancelado por derecho de matrícula y el equivalente de los créditos matriculados, costo aprobado por Consejo Universitario.</li>
                </ul>
            </div>


