            <h2 class="modaltramite">Trámite para la revalidación de Grados y Títulos obtenidos en Universidades Extranjeras</h2>
            
            
            <h2 class="subtitle-general-fix">PAÍSES CON LOS CUALES SE TIENE CONVENIO DE RECIPROCIDAD (1)</h2>
           
            <p class="text-fix-content">
            Argentina, Bulgaria, Bolivia, Colombia, Costa Rica, Cuba, Chile, China, Ecuador, El Salvador, España, Guatemala, Honduras, Hungría, México, Nicaragua, Panamá, Paraguay, Rumania, Uruguay, Venezuela, Santa Sede, Ucrania, Rusia) Presentar su expediente completo en la Asamblea Nacional de Rectores, el cual deberá constar de los siguientes documentos:</p>
           
            
                <ul class="fix-items-li">
                    <li>Solicitud dirigida al Presidente de la Asamblea Nacional de Rectores (adjuntar original y copia simple).</li>
                    <li>Diploma original que acredite el Grado o Título con las firmas legalizadas por el Ministerio de Relaciones Exteriores del país de origen, por el Consulado Peruano del país de origen y por el Ministerio de Relaciones Exteriores del Perú. Traducción Oficial (original y copia legalizada) cuando el título no está en castellano o cuando no sean de habla hispana.</li>
                    <li>Tres copias legalizadas del Grado o Título, anverso y reverso (tamaño A4).</li>
                    <li>Original y una copia legalizada del Certificado de Estudios, con las firmas legalizadas por el Ministerio de RR.EE. del país de origen, Consulado Peruano del país de origen y por el Ministerio de RR.EE. del Perú. Traducción Oficial (original y copia legalizada) cuando el certificado no está en castellano.</li>
                    <li>Una copia legalizada del D.N.I. con la última votación o del carné vigente de extranjería, si se trata de un ciudadano extranjero.</li>
                    <li>Dos fotos de frente tamaño pasaporte.</li>
                    <li>Recibo de pago de derechos por la suma correspondiente (preguntar por el costo actual).</li>
                    <li>Cuando se trate de un grado de maestría y/o doctorado deberá adjuntar la tesis, original y copia (traducidas al español).</li>
                    <li>Constancia original de Trabajo. (Acreditar como mínimo 03 años de experiencia laboral en la especialidad, después de haber egresado).</li>
                </ul>
            
            <p class="text-fix-content mt-3">
            Las legalizaciones son hechas por el Notario Público.
           <br>
            El diploma será recogido personalmente o por Carta con Legalización Notarial.
            <br>
            Sólo serán admitidos los expedientes que cumplan con los requisitos antes indicados.
           <br>
            <b>(1).</b> Fuente: Asamblea Nacional de Rectores
            </p>
            


            <hr>
            <h2 class="subtitle-general-fix">PAÍSES CON LOS CUALES NO SE TIENE CONVENIO DE RECIPROCIDAD</h2>
            
            <p class="text-fix-content">
            Los requisitos para la revalidación de grados y títulos obtenidos en Universidades extranjeras con los cuales el Perú NO tiene Convenio de Reciprocidad son los siguientes:</p>
            
            
                <ul class=fix-items-li>
                    <li>Solicitud dirigida al Rector de la Universidad de San Martín de Porres.</li>
                    <li>Diploma que acredite el Grado o Título con las firmas legalizadas por el Consulado del Perú en el país de origen y el Ministerio de Relaciones Exteriores. Si el diploma no estuviera en castellano debe acompañarse la traducción oficial o traducción simple con la indicación y suscripción del traductor debidamente identificado</li>
                    <li>Tres copias fotostáticas legalizadas notarialmente y autenticadas del Diploma, anverso y reverso y en tamaño oficio.</li>
                    <li>Una copia legalizada del Certificado de Estudios, en el que se acredite asignaturas, créditos y los calificativos correspondientes.</li>
                    <li>Copia fotostáticas y traducidas oficialmente al castellano, si fuera el caso de los certificados de estudios, sílabos o créditos de las asignaturas correspondientes, o traducción simple con la indicación y suscripción del traductor debidamente identificado.</li>
                    <li>Copia fotostática legalizada del DNI o del Carné de Extranjería, si se tratara de un ciudadano extranjero.</li>
                    <li>Recibo de pago por derechos, de acuerdo con el tarifario vigente de la Universidad.</li>
                </ul>
            
        
