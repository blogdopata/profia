        <h6 style="margin-left:20px;" data-toggle="collapse" data-target="#collapseServiciosSTI">
          <a href="#">Servicios <i class="fa fa-angle-double-down"></i></a>
        </h6>

        <div id="collapseServiciosSTI" class="collapse listafia" >
         El área de Servicios de TI, se encarga de atender las necesidades y requerimientos tecnológicos, no solo de la Facultad de Ingeniería y Arquitectura, sino también de las diferentes sedes y facultades que conforman la USMP. Los servicios brindados se pueden dividir en dos categorías: servicios de infraestructura y servicios de red. <br> <br>
         <h6 style="font-size:12px;">Servicios de red</h6> 
             <ul>
                    <li>El área de STI, se ha encargado de establecer un diseño de red que sea modular y de fácil manejo ante cualquier situación, basado en la arquitectura “SONA”. Debido a la adaptabilidad que ofrece el diseño planteado, el área de STI está en búsqueda de homologar todas las redes que integran cada una de las sedes y facultades de la USMP.</li>
                    <li>Servicios de TI, está encargado de diseñar y evaluar el equipamiento de red que se deba adquirir ante la construcción de un nuevo edificio, local o campus. Asimismo, se encarga de evaluar los recambios de equipamientos de red para mantener el parque tecnológico de última generación. Adicionalmente, se encarga de generar la configuración de los diferentes equipos de comunicación que van directamente de la mano con la arquitectura planteada.</li>
                    <li>Servicios de TI, está encargado de generar todas las políticas de seguridad que protegen a los servidores y usuarios de la USMP, para mantener una red saludable y una operatividad continua.</li>
                    <li>Asesorar en la configuración de programas cliente para acceso a servicios de Internet (Correo, Web, FTP, Listas).</li>
                    <li>Servicios de TI, también brinda capacitaciones en la administración de red y seguridad al personal de tecnología de las diferentes sedes y facultades de la USMP. </li>
             </ul>
             <br><br>
