                         <h6 class="subtitle-general-fix mt-3 ">Reglamento para Compromiso de Excepción</h6>
                         (EVALUACIÓN DE RENDIMIENTO MÍNIMO DEL ALUMNO)

                         
                         <h6 class="subtitle-general-fix mt-3 ">Artículo 1</h6>
                         El alumno perderá su condición de estudiante de acuerdo a lo establecido por el Estatuto de la Universidad de San Martín de Porres, en su artículo No. 23 inciso b) y por las razones que se indica en el artículo 24:
                         
                         
                             <ul class="ml-5 fix-items-li mt-3 mb-3">
                             <li>a)	Haber desaprobado tres (03) veces consecutivas una misma asignatura. </li>
                             <li>b)	Haber desaprobado dos (02) veces consecutivas tres asignaturas.</li>
                             <!-- <a href="legal/legalmodelo.php">modelo de ley</a> -->
                             </ul>


                         
