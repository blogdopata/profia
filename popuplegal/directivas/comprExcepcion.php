                         <h6 class="subtitle-general-fix mt-5">Artículo 2 </h6>
                         La excepcionalidad a la aplicación de lo dispuesto en el artículo No. 1 de este reglamento se denominará “COMPROMISO DE EXCEPCIÓN”.
                         
                         <h6 class="subtitle-general-fix mt-5">Artículo 3</h6>
                         Se define como “COMPROMISO DE EXCEPCIÓN ” al documento elaborado por la Comisión de Rendimiento Mínimo, con el fin de registrar mediante la firma del alumno y del padre de familia o apoderado (el cual presentará sus poderes por escrito y debidamente notariado), el compromiso de poner el máximo esfuerzo familiar y personal, para garantizar la aprobación de la o las asignaturas en las que incurrió en el artículo No.1 de este reglamento y no ser separado definitivamente de la Facultad por deficiencia académica.
                        
                         <h6 class="subtitle-general-fix mt-5">Artículo 4 </h6>
                         La Comisión de Rendimiento Mínimo es aquella que está integrada por 3 profesores de la Facultad, de preferencia profesores ordinarios. Es la encargada de llevar a cabo la evaluación del alumno que se encuentra comprendido en los alcances de lo dispuesto en el artículo No.1 de este reglamento, con la participación en las entrevistas de los padres o apoderados.
                         
                         <h6 class="subtitle-general-fix mt-5">Artículo 5 </h6>
                         Los miembros de la Comisión de Rendimiento Mínimo serán designados anualmente por el Decano de la Facultad.
                        
                         <h6 class="subtitle-general-fix mt-5">Artículo 6 </h6>
                         El Ciclo de Verano o nivelación no será tomado en cuenta para efectos del cómputo del número de veces que el alumno desapruebe una o más asignaturas.
                        
                         <h6 class="subtitle-general-fix mt-5">Artículo 7 </h6>
                         Los requisitos académicos y administrativos que se establecen para este proceso son :
                         
                         
                            <ul class="ml-5 fix-items-li mt-3">
                              <li>No adeudar a la Facultad.</li>
                              <li>Solicitud Valorada, adjuntando Histórico de Pagos y Récord de Notas.</li>
                            </ul>
                          
                          
                          <h6 class="subtitle-general-fix mt-5">Artículo 8 </h6>
                          La Comisión de Rendimiento Mínimo se reunirá con el alumno y el padre o apoderado, en las fechas programadas.
                          
                          <h6 class="subtitle-general-fix mt-5">Artículo 9 </h6>
                          En caso de ser un alumno que labora y solvente sus estudios, deberá presentar en la reunión que sostenga con la Comisión de Rendimiento Mínimo, lo siguiente :
                          
                          
                            <ul class="ml-5 fix-items-li mt-3 mb-3">
                              <li>Constancia de Trabajo.</li>
                              <li>Boletas de pago de los dos (02) últimos meses.</li>
                            </ul>
                         
                          
                          Estos requisitos son indispensables para la no exigencia de la presencia del padre o apoderado.
                         
                          <h6 class="subtitle-general-fix mt-5">Artículo 10 </h6>
                          Luego de la evaluación respectiva realizada por la Comisión de Rendimiento Mínimo, se emitirá el dictamen final con carácter de inapelable. En caso de dar su aprobación para que el alumno se matricule por ultima vez en las asignaturas desaprobadas, deberán firmar ambas partes el COMPROMISO DE EXCEPCIÓN.
                          
                          <h6 class="subtitle-general-fix mt-5">Artículo 11 </h6>
                          Los Alumnos matriculados por última vez en los cursos con COMPROMISO DE EXCEPCIÓN y que resulten desaprobados serán separados definitivamente de la Facultad por deficiencia académica.
                         
                          <h6 class="subtitle-general-fix mt-5">Artículo 12 </h6>
                          La Oficina de Registros Académicos al final de cada Semestre Académico, alcanzará al Decano de la Facultad la relación de los alumnos que el Sistema Académico automáticamente haya separado por deficiencia académica; para la emisión de la respectiva Resolución Decanal.
                          
                          <h6 class="subtitle-general-fix mt-5">Artículo 13 </h6>
                          La Oficina de Registros Académicos al final de cada Semestre Académico entregará a la Comisión de Rendimiento Mínimo la relación de alumnos que han desaprobado tres (03) veces una misma asignatura o dos (02) veces consecutivas tres o más asignaturas.
                        
                          <h6 class="subtitle-general-fix mt-5">Artículo 14 </h6>
                          La Oficina de Registros Académicos matriculará al alumno con COMPROMISO DE EXCEPCIÓN, teniendo en cuenta las mejores opciones académicas para el alumno (turnos y secciones).
                          
                          
                          <h6 class="subtitle-general-fix mt-5">Artículo 15 </h6>
                          El alumno con COMPROMISO DE EXCEPCIÓN podrá matricularse sólo en la o las asignaturas afectadas o hasta 12 créditos como máximo según sea el caso.
                        
                          <h6 class="subtitle-general-fix mt-5">Artículo 16 </h6>
                          Los alumnos que cuentan con COMPROMISO DE EXCEPCIÓN y no pudieran matricularse por razones justificadas, podrán realizar el proceso de reserva de matricula previa autorización de la Comisión de Rendimiento Mínimo por un solo semestre académico, caso contrario se aplicará el artículo No.1 de este reglamento.
                        
                          <h6 class="subtitle-general-fix mt-5">Artículo 17 </h6>
                          Los alumnos con COMPROMISO DE EXCEPCIÓN y que se encuentren Matriculados en un semestre vigente no podrán realizar los procesos de retiro de Ciclo y/ o de Cursos afectados.
                          
                          <h6 class="subtitle-general-fix mt-5">Artículo 18 </h6>
                          La escala de pagos del alumno matriculado con COMPROMISO DE EXCEPCIÓN, ascenderá a la escala máxima de la Escuela a que pertenece. Una vez aprobada(s) la(s) asignatura(s), regresará a su escala original, es decir a la que tenía antes de estar inmerso en el Proceso mencionado.
                         
                          <h6 class="subtitle-general-fix mt-5">Artículo 19 </h6>
                          La Comisión de Rendimiento Mínimo comunicará periódicamente a los Padres del alumno matriculado con COMPROMISO DE EXCEPCIÓN, sobre su rendimiento académico y asistencia.
                         
                          <h6 class="subtitle-general-fix mt-5">Artículo 20 </h6>
                          Cualquier caso no contemplado en el presente reglamento y las excepciones que se presenten, serán absueltas por la Comisión de Rendimiento Mínimo.
































