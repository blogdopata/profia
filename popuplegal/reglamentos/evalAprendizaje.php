        <h6 class="subtitle-general-fix">Capítulo I  <br>De la Evaluación del Aprendizaje</h6> 

        <h6 class="subtitle-general-fix mt-5">Artículo 1°</h6>

        La evaluación del proceso de enseñanza-aprendizaje es un juicio de valor que refleja los logros y las deficiencias de la enseñanza y del aprendizaje, se fundamenta en mediciones y descripciones cualitativas y orienta la planificación mediata e inmediata del trabajo académico.

        <h6 class="subtitle-general-fix mt-2">Artículo 2°</h6>
        La evaluación del aprendizaje se propone los siguientes objetivos:
        <ul class="fix-items-li ml-5">
          <li>Establecer en forma sistemática los niveles del logro individual y colectivo del proceso de enseñanza-aprendizaje y proporcionar información pertinente para la promoción de los estudiantes.</li>
          <li>Conocer las potencialidades y limitaciones de cada estudiante.</li>
          <li>Obtener la información necesaria para la formulación de las acciones de recuperación que requiera el alumno o grupos de alumnos.</li>
        </ul>

        <h6 class="subtitle-general-fix mt-2">Artículo 3°</h6>
        La evaluación del aprendizaje cumple las siguientes funciones:

        <ul class="fix-items-li ml-5 ">
          <li>Proporcionar información sobre el estado inicial de los estudiantes en los dominios cognoscitivo, socioafectivo y psicomotor para aplicar las estrategias pedagógicas correspondientes.</li>
          <li>Brindar información pertinente y oportuna para tomar decisiones en relación con los sujetos, procesos y elementos del currículo orientados a su mejoramiento.</li>
          <li>Fundamentar la calificación y acreditación del rendimiento académico de los alumnos.</li>
        </ul>


        <h6 class="subtitle-general-fix mt-2">Artículo 4°</h6>
        La evaluación tiene las características siguientes:
        <ul class="fix-items-li ml-5">
          <li>Es integral porque toma en cuenta todos los aspectos del desarrollo de la personalidad del alumno: el cognoscitivo, el socioafectivo y el psicomotor.</li>
          <li>Es continua porque se manifiesta permanentemente durante el proceso educativo.</li>
          <li>Es acumulativa porque recupera y orienta, en los respectivos registros, la información cualitativa y cuantitativa que debe ser analizada para mejorar los propios rendimientos.</li>
          <li>Es valorativa porque es un acto de conciencia que ejecuta el educador, bajo su responsabilidad y ética profesional, sustentado en información cualitativa y cuantitativa que él mismo recupera de manera sistemática a través de procedimientos, técnicas e instrumentos confiables, objetivos y válidos.</li>
          <li>Es flexible porque se adecua a las condiciones y circunstancias específicas de la realidad de los sujetos y elementos del currículo.</li>
        </ul>

