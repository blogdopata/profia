              <h6 class="subtitle-general-fix mt-5">Capítulo II <br>De los Requisitos</h6>

              <h6 class="subtitle-general-fix mt-4">Artículo 12°</h6>
              Los alumnos de la Universidad de San Martín de Porres solo podrán solicitar convalidaciones de asignaturas, en caso de haber realizado traslado interno o solicitar reactualización de matrícula, en las siguientes circunstancias:
              <ul class="fix-items-li   ml-5">
                <li>Cuando se inician los estudios de un semestre lectivo</li>
                <li>Cuando se matriculan por primera vez en la facultad correspondiente</li>
              </ul>



              <h6 class="subtitle-general-fix mt-4">Artículo 13°</h6>
              Para solicitar la convalidación, el alumno de la universidad debe presentar los siguientes documentos:


              <ul class="fix-items-li   ml-5">
                <li>Los certificados de estudios originales emitidos por la facultad de procedencia</li>
                <li>Los sílabos de las asignaturas por convalidar, sellados y firmados por el Secretario de la Facultad de origen, solo en caso de traslados</li>
              </ul> 
              Asimismo, el alumno debe efectuar el trámite dentro de las fechas establecidas en el cronograma de matrícula respectivo.




              <h6 class="subtitle-general-fix mt-4">Artículo 14°</h6>
              Para solicitar la convalidación, los alumnos que provienen de universidades nacionales o privadas por traslado externo, deben presentar los siguientes documentos:

              <ul class="fix-items-li   ml-5">
               <li>Certificados de estudios originales debidamente autenticados</li>
               <li>Los sílabos de las asignaturas por convalidar, debidamente autenticados por la Secretaría de la Facultad respectiva</li>
              </ul>


              <h6 class="subtitle-general-fix mt-4">Artículo 15°</h6>
              A los alumnos que provienen de institutos superiores se les exige los siguientes documentos:



              <ul class="fix-items-li   ml-5">
               <li>Título profesional debidamente autenticado por la institución de origen, si el ingreso fuera por la modalidad de Complementación Académica</li>
               <li>Certificados originales de los estudios profesionales</li>
               <li>Sílabos de las asignaturas por convalidar debidamente autenticados por la oficina correspondiente</li>
              </ul>



              <h6 class="subtitle-general-fix mt-4">Artículo 16°</h6>
              A los alumnos que provienen de universidades extranjeras se les exige los siguientes requisitos:

              <ul class="fix-items-li   ml-5">
                <li>Acreditación de la universidad de origen</li>
                <li>Certificado de estudios visados por el Ministerio de Relaciones Exteriores del país de origen y el Consulado respectivamente. Si el certificado de estudios se encuentra en un idioma distinto al español, debe ser traducido oficialmente.  </li>
                <li>Sílabos de las asignaturas por convalidar debidamente autenticados por la institución</li>
                <li>Documento de residencia en el Perú debidamente certificado</li>
                <li>Dominio del idioma español.</li>
              </ul>



              






















