              <h6 class="subtitle-general-fix mt-3">Reglamento de Convalidación de Asignaturas</h6>
              <p style="text-transform: uppercase;">(Aprobado por Resolución Rectoral Nro. 1180-2001-CU-R-USMP de fecha 27 de setiembre de 2001) 
              </p>

              <h6 class="subtitle-general-fix mt-2">Capítulo I <br>Naturaleza, Fines y Criterios de la Convalidación</h6>

              <h6 class="subtitle-general-fix mt-4">Artículo 1°</h6>
              Constituyen bases legales y normativas del presente reglamento los dispositivos siguientes:


              <ul class="fix-items-li mt-3 ml-5">
                <li>Ley Universitaria, Ley N° 23733 (artículos 55° y 56°)</li>
                <li>Estatuto de la Universidad de San Martín de Porres (artículos 15° inciso A y 16°). Resolución Rectoral Nro. 658-2000-AU-R- SMP del 25 de agosto de 2000</li>
                <li>Reglamento de la Oficina de Admisión de la Universidad de San Martín de Porres (artículo 9, último parágrafo; 53° -B): Resolución Rectoral Nro. 1033-2000-CU-R-SMP de 20 de diciembre de 2000</li>
                <li>Reglamento de la Oficina de Registros Académicos de la Universidad de San Martín de Porres (artículos 93° a 110°). Resolución Rectoral Nro. 987-97-R-SMP del 30 de octubre de 1997</li>
                <li>Reglamento de Evaluación del Aprendizaje (artículo 6°). Resolución Rectoral Nro. 539-2000-CU-R-SMP del 3 de agosto de 2000</li>
                <li>Normas para la Reactualización de la Matrícula (artículo 2°). Resolución Rectoral N° 539-2000-CU-R-SMP del 3 de agosto de 2000</li>
              </ul>


              <h6 class="subtitle-general-fix mt-4">Artículo 2°</h6>
              La convalidación de asignaturas es una actividad académico administrativa que la universidad ejecuta, a través de sus facultades, con el propósito de evaluar los expedientes, dar por cursadas y aprobadas las asignaturas y determinar la situación curricular de los alumnos que ingresen por la modalidad de Exoneración al Concurso Ordinario de Admisión por poseer Título Profesional o haber realizado traslado interno o externo; y, de aquellos que soliciten reactualización de matrícula para adecuarlos a un plan curricular vigente. 

              <h6 class="subtitle-general-fix mt-4">Artículo 3°</h6>
              Tienen derecho a solicitar convalidación de asignaturas:  <br><br>
              Los que posean Título Profesional o Grado Académico obtenidos en la universidad, en otras universidades nacionales o extranjeras o en otros centros de educación de nivel superior reconocidos por la USMP.  <br>
              Quienes hayan aprobado en la universidad, en otras universidades nacionales o extranjeras o en centros educativos de nivel superior, reconocidos por la universidad, por lo menos dos (02) períodos lectivos semestrales completos o uno anual o (36) treinta y seis créditos. <br>
              Quienes soliciten reactualización de matrícula, en este caso se tendrá en cuenta el tiempo que el alumno ha dejado de estudiar.  



              <h6 class="subtitle-general-fix mt-4">Artículo 4°</h6>
              Son fines de la convalidación de asignaturas:
              <ul class="fix-items-li ml-5">
                <li>Revalidar las asignaturas y/o actividades curriculares aprobadas con similar denominación, contenido y peso en créditos, de acuerdo con el plan curricular vigente.</li>
                <li>Ubicar al alumno ingresante, de acuerdo con la convalidación efectuada, en el ciclo académico que le corresponda del plan curricular vigente de la carrera.</li>
              </ul>


              <h6 class="subtitle-general-fix mt-4">Artículo 5°</h6>
              Son criterios para efectuar la convalidación de asignaturas:<br><br>
              Similar denominación, contenido y peso en créditos de la asignatura. <br>
              Denominación diferente, pero afinidad en cuanto a contenido temático como mínimo en un 70%, en relación con la asignatura a convalidar. <br>
              Juicio de experto, cuando se trate de asignaturas de especialidad y el o los profesores encargados de la convalidación no tengan los suficientes elementos de juicio académico para determinar la pertinencia o no del derecho invocado. <br>
              En el caso de haber aprobado dos asignaturas similares y/o afines, con menor peso en créditos al existente en la facultad, se realizará la suma y se le dará la equivalencia correspondiente. <br>
              En caso de la Facultad de Medicina Humana solo se podrán convalidar asignaturas aprobadas en los últimos tres (03) años; si las asignaturas hubieran sido aprobadas con una antigüedad mayor a (03) años y menor a cinco (05) años, el postulante deberá someterse a un examen de conocimientos de la materia respectiva.


              <h6 class="subtitle-general-fix mt-4">Artículo 6°</h6>
              Las calificaciones serán tomadas directamente del certificado de estudios original expedido por la universidad o el instituto superior de origen, siendo el único documento válido para la convalidación.


              <h6 class="subtitle-general-fix mt-4">Artículo 7°</h6>
              Si el sistema de calificación acreditado en el certificado de estudios fuera centesimal o literal, para efectos de convalidación, deberá ser convertido mediante una tabla de equivalencias al sistema vigesimal.


              <h6 class="subtitle-general-fix mt-4">Artículo 8°</h6>
              Para la calificación con sistema centesimal, se toma en cuenta que la nota aprobatoria mínima es de cincuenta y cinco (55) equivalente a once (11), y las demás notas se van contabilizando de cinco en cinco para adaptarse al sistema vigesimal.

              <h6 class="subtitle-general-fix mt-4">Artículo 9°</h6>
              Para la calificación con sistema literal, se aplica el Sistema Americano de Equivalencias, que considera la siguiente escala cualitativa: 

              <br><br>
              <ul class="fix-items-li   ml-5">
                <li>E = Desaprobado</li>
                <li>D = Deficiente</li>
                <li>C = Regular</li>
                <li>B = Bien</li>
                <li>A = Excelente</li>
              </ul>


              <h6 class="subtitle-general-fix mt-4">Artículo 10°</h6>
              Reducido el sistema literal al vigesimal, resultan las siguientes equivalencias: 


              <br><br>
              <ul class="fix-items-li   ml-5">
                <li>E = 0 - 10</li>
                <li>D = 11 - 12</li>
                <li>C = 13 - 15</li>
                <li>B = 16 - 18</li>
                <li>A = 19 - 20</li>
              </ul>
              


              <h6 class="subtitle-general-fix mt-4 ">Artículo 11°</h6> 
              En el caso singular que figure en los certificados de estudios, en vez de la calificación numérica o literal, la palabra “convalidado”, queda autorizado el profesor a cargo de la convalidación para tomar como fuente de referencia la nota numérica de la institución de origen que dio lugar a la palabra o expresión similar “convalidado”. Igual criterio se aplicará si apareciera en el certificado de estudios la palabra “aprobado” o “desaprobado”. 
