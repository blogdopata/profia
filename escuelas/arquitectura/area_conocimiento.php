<p>
  <a class="hoverarqui" data-toggle="collapse" href="#acyr" role="button" aria-expanded="false" aria-controls="collapseExample">
   Área de comunicación y representación <i id="azulfia" class="fa fa-arrow-circle-o-down"></i>
  </a>
</p>
<div class="collapse" id="acyr">
  <div class="card card-body" style="text-align:justify;">
    Construir una estructura mental para estimular la observación, el registro y la representación como base para la comprensión compleja del espacio. <br> <br>


Transmitir los métodos y ejercitar las técnicas de representación sin exclusión alguna, como herramientas de ideación, comunicación y presentación de proyectos de diseño arquitectónico y urbano. <br> <br>


Desarrollar la capacidad de representar el espacio en proyecciones bidimensionales y tridimensionales. <br> <br>

Discriminar y utilizar las técnicas gráficas, modélicas y planimétricas apropiadas a las distintas etapas del proceso proyectual. <br> <br>


Entender la informática como recurso y campo de experimentación de nuevas lógicas proyectuales, que además de agilizar los procesos, potencia la exploración y modelación tridimensional, el ensayo de condicionantes y variables, la producción de prototipos y la fabricación digital. <br> <br> 


Capacidad creativa y de abstracción para expresar cualidades espaciales, fomentando una cultura de comunicación visual. <br> <br>

Manejar formatos y estándares de presentación internacional. <br> <br>


Lograr una expresión propia y la capacidad de elaborar un portafolio personal. <br> <br>
