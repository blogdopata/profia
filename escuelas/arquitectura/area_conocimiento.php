<p>
  <a class="hoverarqui" data-toggle="collapse" href="#ahtc" role="button" aria-expanded="false" aria-controls="collapseExample">
   Área de historia, teoría y crítica <i id="azulfia" class="fa fa-arrow-circle-o-down"></i>
  </a>
</p>
<div class="collapse" id="ahtc">
  <div class="card card-body" style="text-align:justify;">
    Explorar mediante el debate y la confrontación intelectual las ideas, proyectos y obras significativas, con énfasis en lo contemporáneo. <br><br>


Articular reflexiones más allá de la simple ubicación cronológica para relacionar problemáticas que superen la imagen arquitectónica. <br><br>


Adquirir una cultura arquitectónica, construir un pensamiento reflexivo y crítico capaz de emitir juicios fundamentados respecto la disciplina. <br><br>


Relacionar la historia, la teoría y la crítica como fundamento para la elaboración del proyecto. <br><br>


Incorporar el conocimiento del contexto social y cultural y valorar la producción arquitectónica local y patrimonial. <br><br>


Explicar las circunstancias que rodean todo el proceso del proyecto y conocer evolución de la arquitectura como disciplina y profesión. <br>
  </div>
</div>



<p>
  <a class="hoverarqui" data-toggle="collapse" href="#adc" role="button" aria-expanded="false" aria-controls="collapseExample">
   Área de diseño y ciudad <i id="azulfia" class="fa fa-arrow-circle-o-down"></i>
  </a>
</p>
<div class="collapse" id="adc">
  <div class="card card-body" style="text-align:justify;">
    Los cursos de Taller debe evidenciar la progresión del desempeño proyectual mediante temáticas que remitan a los descriptores de cada etapa del plan de estudios, estas no deben ser entendidas como una fragmentación del hecho proyectual, sino como activadores que permitan superar respuestas evidentes, sin perder el carácter unitario. <br>

Es el lugar donde las ideas se materializan, toman consistencia intelectual y forma física garantizando que en ese transcurrir se conoce y produce la arquitectura. Es un modo de enseñanza–aprendizaje, donde se aprende haciendo, a través del trabajo y reflexión entre profesores y alumnos en torno a conceptos disciplinares, donde los procesos son tan importantes como los resultados, superándose la imitación de la práctica profesional y la complejidad por el incremento de escala y cantidad de planimetría. Es el eje estructurador de la carrera, donde se integran saberes y sintetizan conocimientos de las otras áreas asegurando su aplicación y permitiendo la visión más amplia de la arquitectura, evaluándose las competencias para resolver problemas de diseño. <br>

Las estrategias metodológicas construyen un proceso dinámico y abierto para adquirir y generar conocimiento donde se explora, investiga y simula gradualmente la realidad y complejidad del hecho proyectual con posturas precisas, evitando su resolución arbitraria como tipología  edificatoria.
Los proyectos deben referenciarse a un espacio mayor evidenciando la unión arquitectura-ciudad. <br>

Los cursos de Ciudad deben desarrollar el concepto de ciudadanía y entregar los conocimientos e instrumentos para el estudio y la intervención de la ciudad en sus diversas escalas. <br>

Entender la ciudad como fenómeno cultural y punto de articulación entre la arquitectura y el urbanismo que se aborda de manera multidisciplinaria. <br> 

Estudiar los fenómenos urbanos y territoriales, sus requerimientos medioambientales y de sostenibilidad. <br>

Manejar criterios de diseño del paisaje e intervención y conservación patrimonial. <br>

Conocer las variables que intervienen en el diseño e implementación de proyectos urbanos, reconocer el impacto que generan y entender los planes de ordenamiento y la normatividad no sólo como instrumentos económicos, sino dinamizadores sociales y culturales. <br>

Conocer los sistemas de información geográfica como herramientas de análisis del territorio y paisaje. <br>

Conocer tendencias contemporáneas sobre desarrollo urbano y Ia imagen futura de la ciudad. <br>
