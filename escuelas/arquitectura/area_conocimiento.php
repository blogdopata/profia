    Área de tecnología de la construcción <i id="azulfia" class="fa fa-arrow-circle-o-down"></i>
  </a>
</p>
    <div class="collapse" id="atc">
    <div class="card card-body" style="text-align:justify;">
       Estudiar casos de arquitectura relevantes desde su componente material para desarrollar una lógica del hacer con sensibilidad frente a los materiales y la resolución de los detalles constructivos. <br> <br>

       Entender la tecnología como herramienta de diseño que otorga un pensamiento técnico para tomar decisiones en el proceso proyectual vinculando la arquitectura con la ingeniería. <br> <br>

       Entregar los conocimientos y la práctica respecto de los materiales, procesos, sistemas y técnicas de edificación así como aspectos normativos y de gestión de la construcción. <br> <br>
 
       Entender la estructura como sistema de soporte y conocer los criterios de configuración y pre-dimensionamiento. <br> <br>

       Conocer aspectos de las redes de instalaciones y sistemas mecánicos. <br> <br>

       Conocer aspectos de acondicionamiento y confort ambiental, los fundamentos de la sustentabilidad y la responsabilidad energética. Conocer y desarrollar la planimetría y su terminología técnica para garantizar el cumplimiento de las especificaciones de los requerimientos de estabilidad y durabilidad. <br>
