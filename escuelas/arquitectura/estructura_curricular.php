
<h5 id="grisfia">Estructura Curricular</h5>
<b id="grisfia">Objetivos Académicos</b>
<br>
<p>Los objetivos académicos de la carrera son los siguientes:</p>


<div style="text-align:justify;">

<ul style="margin-left:10px;">
	<li class="liarquitectura">
	<p id="justificar">Desarrollar la experiencia de diseño para resolver problemáticas arquitectónicas de baja, media y alta complejidad, mediante los conceptos de espacio, forma y función.</p>
	</li>
	<li class="liarquitectura">
		<p id="justificar">Desenvolverse adecuadamente con capacidad para aplicar los conceptos aprendidos en la problemática del urbanismo, la ciudad con la ecología y el desarrollo de hábitats, que incluyan lo necesario para dar calidad de vida vinculada a los servicios que requieran para una vida digna en comunidad,: salud, educación, etc.</p>
	</li>
	<li class="liarquitectura">
		<p id="justificar">Practicar un ejercicio profesional responsable, con valores éticos y utilizar de manera adecuada los recursos disponibles.</p>
	</li>
	<li class="liarquitectura">
		<p id="justificar">Trabajar en equipos multidisciplinarios, para desarrollar proyectos integrados y compatibilizados que aporten al desarrollo armónico de la construcción y el bienestar de la sociedad.</p>
	</li>
	<li class="liarquitectura">
		<p id="justificar">Profesar el ejercicio profesional sosteniendo un aprendizaje continuo vinculado al crecimiento personal.</p>
	</li>
</ul>

</div>                       

           
