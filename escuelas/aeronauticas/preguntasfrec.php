<h3 class="subtitle-general-fix" >Preguntas Frecuentes</h3>


<p  class="text-fix-content "> <b> ¿Cuando empiezo a volar?  </b></p>

<p  class="text-fix-content" >
En la EPCA las operaciones aéreas empiezan al finalizar el primero ciclo académico, incluso antes de empezar el segundo ciclo.

 </p>

<p class="text-fix-content "> <b> ¿Es necesario mi examen médico para poder postul</b>ar? 
</p>
<p  class="text-fix-content">

<p  class="text-fix-content" >

Para postular a la EPCA no es necesario haber realizado el examen médico. Se requiere el examen médico para tramitar la licencia de alumno piloto ante la DGAC la misma que es requisito para iniciar las operaciones aéreas (vuelos)

</p>
</p>
 

<p class="text-fix-content "> <b> ¿La hora de vuelo está incluida en la pensión?  </b></p>

<p  class="text-fix-content" >

La hora de vuelo no está incluida en la pensión, tiene un costo adicional y se paga en función a las horas de vuelo que el alumno desee realizar.

 </p>



<p class="text-fix-content "> <b> ¿Es necesario volar para seguir esta carrera?  </b></p>

<p  class="text-fix-content" >
Los alumnos de la EPCA pueden seguir la carrera sin volar y graduarse como Licenciado en Ciencias Aeronáuticas con la mención de Gestión y Seguridad Aeronáutica.

 </p>


<p class="text-fix-content "> <b> ¿Cuántas horas de vuelo necesito para poder obtener mi licencia de Piloto comercial? </b></p>

<p  class="text-fix-content" >
El total de horas de vuelo necesarias para obtener la licencia de piloto comercial son un aproximado de 200 horas.
</p>
 


<p class="text-fix-content "> <b> ¿En cuánto tiempo voy a realizar todas mis horas de vuelo?  </b></p>

<p  class="text-fix-content" >
El tiempo en el cual se concluyen las horas de vuelo está sujeto al pago, la disponibilidad del alumno y las condiciones meteorológicas. En caso de tener estas tres condiciones a favor, las horas de vuelo se estarían concluyendo en un aproximado de año y medio.
</p>
 


<p class="text-fix-content "> <b> ¿Cuánto es el costo de la hora de vuelo?  </b></p>
<p  class="text-fix-content" >
La EPCA ofrece un precio preferencial para sus alumnos de 150 dólares americanos, en un moderno avión Cessna 172 G1000. Es preciso resaltar que este precio por horade vuelo está muy por debajo del precio de mercado.
</p>
 


<p class="text-fix-content "> <b> ¿Puedo graduarme con las 2 menciones? </b></p>
<p  class="text-fix-content" >
Si es posible graduarse con las dos menciones siempre y cuando se concluya el creditaje de ambas.
</p>


