    <p id="justificar">
    Escuela perteneciente a la Facultad de Ingeniería y Arquitectura. Esta carrera busca preparar a los estudiantes en una profesión competitiva, que le permita ejercer una gestión responsable en todas las actividades de la aviación. 
    </p>
    <p id="justificar">
    El egresado de la especialidad de Ciencias Aeronáuticas contará con una sólida formación en gestión de negocios, con enfoque en el sector aeronáutico. 
    </p>
    <p id="justificar">
    Adicionalmente, la carrera le proveerá de todo el fundamento teórico, además de la instrucción y entrenamiento de vuelo necesario para estar apto en la rendición de los exámenes y vuelos de chequeo ante la Dirección General de Aeronáutica Civil (DGAC), y así obtener la licencia de Piloto Comercial. Este objetivo se enmarca en los principios rectores de la Universidad de San Martín de Porres y de la Facultad de Ingeniería y Arquitectura, orientados a la formación de profesionales altamente calificados para satisfacer la demanda que requiere la sociedad.
    </p>

    <h2 class="titulo-generico">MISIÓN</h2>
    <p id="justificar">
    Formar profesionales del más alto nivel para el Mercado Aeronáutico Nacional e Internacional, con sólidos valores, competencias y habilidades para responder a los cambios y desafíos del entorno; y así satisfacer la demanda que requiere la sociedad.
    </p>
    <h2 class="titulo-generico">VISIÓN</h2>
    <p id="justificar">
    Ser la mejor Escuela Profesional de Latinoamérica, dedicada a la formación de profesionales con sólido enfoque en la gestión de negocios y en el pilotaje de aeronaves de transporte de pasajeros y de carga.
    </p>

    <h2 class="titulo-generico">OBJETIVOS EDUCACIONALES</h2>
    <div class="clearfix"></div>
    <br><br><br>
    
    <p id="justificar">
    <b style="font-size:16px;">Objetivo General</b>
    <br>
    <br>
    Formar profesionales de nivel universitario con competencias para gerenciar organizaciones del sector aeronáutico, tanto públicas como privadas, y pilotear de manera segura aviones de pasajeros y de carga, de acuerdo con los estándares internacionales. 
    </p>

    <br>

    <b id="grisfia">Objetivos Específicos</b>
    <br><br>
    <div class="listaperfil2">
    <li>Brindar sólida formación en ciencias básicas y administrativas que permitan gestionar con éxito en el ámbito corporativo, cualquier empresa y en particular las del sector aeronáutico.</li>

    <li>Formar pilotos comerciales capaces de volar de manera segura aviones de transporte de pasajeros y de carga.</li>

    <li>Desenvolverse adecuadamente con capacidad analítica y comunicativa para aportar soluciones que agreguen valor a la organización.</li>

    <li>Practicar un ejercicio profesional responsable, con valores éticos y utilizar de manera adecuada los recursos disponibles en la organización.</li>

    <li>Ser un profesional comprometido con el aprendizaje continuo para su desarrollo personal a lo largo de toda la vida.</li>
    <br>
    
    <b id="grisfia">Objetivos Educativos</b>
    <br>
    <br>
    
    <p id="justificar">
    Los objetivos educativos de la Escuela Profesional de Ciencias Aeronáuticas son las competencias, habilidades y atributos que se espera que los graduados logren dentro de un período de dos a diez años después de la graduación.
    </p>
    
    <p>Los objetivos educativos son los siguientes: </p>
    <div class="listaperfil2">
    <li>Profesionalismo: Lograr y ejercer los más altos estándares de integridad personal y profesional. La toma de decisiones humanísticas, éticas, morales, cívicas y profesionales relacionadas con el campo aeronáutico y cumplir con las regulaciones nacionales e internacionales establecidas.</li>

    <li>Multi-Disciplinario: Ejercer adaptabilidad de alto grado, ser capaz de trabajar en diferentes situaciones y funciones con resultados competentes. Demostrando resultados de alta calidad en equipos multidisciplinarios y multi-culturales.</li>

    <li>Aprendizaje Permanente: Que el alumno internalice, acepte y se comprometa con la educación continua a través de programas de educación superior, oportunidades de capacitación laboral y auto-educación basadas en los requisitos de la industria de la aviación".</li>
    </div>
