    <div id="collapse5" class="collapse text-fix-content" aria-labelledby="heading5" data-parent="#accordionC">
      <div class="card-body" style="color:#575858;">
      El desarrollo de la actividad productiva de pisco se viene fortaleciendo en el marco de campañas de promoción en el mercado externo e interno, promovido mayormente por empresarios agroindustriales (de productores y de plantas de procesamiento), lo que se ha manifestado por un incremento bastante significativo en nuestras exportaciones en el periodo 2003-2004. <br><br> Es conveniente remarcar que el posicionamiento de nuestro producto de bandera será posible en tanto se logre fortalecer la "cadena productiva del pisco" y desarrollar tecnologías nacionales de avanzada de manera de lograr su rentabilidad y sostenibilidad, que estará determinada por la satisfacción de las expectativas de los agentes responsables: productores, centros de procesamiento y del mercado.
        <br><br> Justamente la destilación que es una parte importante del proceso presenta deficiencias que afectan la calidad del producto final de muchas empresas, asimismo también afectan la falta de estandarización del pisco peruano respecto a la presencia de componentes aromáticos y volátiles en concentraciones definidas y de su grado alcohólico. <br><br>
        Este problema tecnológico arriba señalado ha sido abordado en forma integral por un grupo de investigadores y profesionales del más alto nivel en este tema y han logrado desarrollar tecnologías de avanzada e innovaciones tecnológicas como un aporte significativo a nuestra industria nacional del pisco. <br><br> Se ha logrado la automatización digital del control de variables en el proceso de destilación del pisco; para lo cual se construyó un prototipo de destilador similar al de uso comercial en el marco de la norma establecida por INDECOPI, dotando a éste de mecanismos e instrumentos de control de variables del proceso de destilación: temperatura de calentamiento del mosto, mecanismos de transferencia de calor, temperaturas de los vapores generados así como la temperatura de condensado; complementado con la operación de separación de las fracciones "cabeza", "cuerpo" y "cola" en función a variables temperatura, tiempo y control de grado alcohólico, situación que permitirá estandarizar la calidad del pisco a nivel de bodegas. <br><br>
        Se ha logrado que todo este proceso este automatizado y con las diferentes pruebas y control de calidad realizados, actualmente este proyecto piloto esta en condiciones de replicarse a nivel industrial por todas las empresas interesadas en mejorar su producción y ser competitivas.

