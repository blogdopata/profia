      Actualmente la industria metalmecánica nacional no fabrica equipos con control y operación automatizados para el proceso de deshidratado de productos agropecuarios. <br><br> De otro lado, nuestro país se ha convertido en importador de frutas deshidratadas principalmente de Chile y Argentina que al año 2000, bordean las 5,000 TM de dichos productos deshidratados.
        <br> Frente a esta problemática el grupo de investigadores y profesionales de la USMP deciden realizar un proyecto de investigación tecnológica en este tema y gracias a la labor constante y decidida de este grupo se ha logrado realizar un proyecto con algunos aportes e innovaciones tecnológicas para el país. <br><br> El producto final es una innovación tecnológica de una nueva deshidratadora de cabina que tiene un sistema electrónico de control de los parámetros del proceso de deshidratación para diversos productos como frutas y hortalizas, optimizando el sistema de circulación del aire y el control automático de la temperatura y humedad relativa. <br><br> Esta cabina permite lograr una homogenización del aire al interior de la cámara de secado, control digital de temperaturas por etapas programadas, control digital de la humedad relativa de la cámara de secado y un programa de secado preestablecido en función del producto a procesar, humedad inicial del producto y humedad final óptima. <br><br> Es decir una programación del proceso hasta en 6 etapas con temperaturas diferentes de acuerdo a las necesidades y características de las materias primas. <br><br> La universidad cuenta con patente de esta innovación tecnológica y actualmente esta deshidratadora automática ya esta a disposición de las empresas.

