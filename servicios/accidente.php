                             <b id="grisfia" class="text-fix-style">Principales Exclusiones</b>
                             <div class="listaperfil2">
                             <li>Cualquier enfermedad corporal o mental, y a las consecuencias de tratamientos médicos o quirúrgicos que no sean motivados por accidentes amparados por la presente Póliza.</li>
                             <li>Los llamados 'Accidentes Médicos', tales como, pero no limitado a apoplejías, congestiones , sincopes vértigos, edemas agudos, infarto al miocardio, trombosis y ataques epilépticos; así como los casos de negligencia o impericia médica.</li>
                             <li>Los accidentes ocurridos cuando el ASEGURADO se encuentra bajo la influencia de alcohol y/o drogas (sin importar su grado de presencia en la sangre) o en estado de sonambulismo.</li>
                             <li>Las lesiones preexistentes y/o tratamientos de lesiones preexistentes al momento de contratar este Seguro.</li>
                             <li>Las consecuencias de hernias y enredamientos intestinales, de insolaciones, inhalación de gases, intoxicaciones químicas y/o alimenticias y envenenamiento sistemáticos.</li>
                             <li>Pena de muerte o participaciones activa en cualquier acto delictivo o en actos violatorios de leyes o reglamentos; en el duelo concertado; en peleas o riñas, salvo en aquellos casos en que se acredite legítima defensa; servicios militar; así como participación activa en actos notoriamente peligrosos, tales como: huelga, motín, conmoción civil, daño malicioso, vandalismo y terrorismo.</li>
                             <li>Las complicaciones médicas de embarazos o partos.</li>
                             <li>Suicidio, automutilación o autolesión.</li>
                             <li>Por acto delictivo contra el ASEGURADO cometido en calidad de autor o cómplice por el beneficiario o heredero, dejando a salvo el derecho de recibir el capital garantizado de los restantes beneficiarios o herederos, si los hubiere, así como su derecho a crecer.</li>
                             <li>Guerra, invasión u operaciones bélicas, actos hostiles de entidades soberanas o del gobierno, guerra civil, rebelión, revolución, insurrección, conmoción civil que adquiera las proporciones de un levantamiento, poder militar o usurpado o ley marcial o confiscación por orden de un Gobierno o autoridad pública.</li>
                             <li>Detonación nuclear, reacción, radiación nuclear o contaminación radiactiva, independientemente de la forma en que se haya ocasionado la detonación nuclear, reacción, radiación nuclear o contaminación radiactiva, así como la emisión de radiaciones ionizantes o contaminación por radioactiva de cualquier combustible nuclear o de cualquier desperdicio proveniente de la combustión de dicho combustible.</li>
                             <li>Los accidentes que se produzcan en la práctica de actividades y deportes notoriamente peligrosos que no guarden relación con la actividad u ocupación declarada por el ASEGURADO, considerando dentro de esta exclusión :conducción de automóviles o vehículos de competencia , así como la participación en carreras de automóviles , motos , motonetas , bicicletas, motocross y similares, la participación en concursos o prácticas hípicas ,alpinismo o andinismo , cacería , pesca submarina o en alta mar, ala delta , parapente, puenting, paracaidismo, buceo, escalada, esquí, surfing y otros deportes de riego.</li>
                             <li>Actos que infrinjan las leyes o reglamentos vigentes.</li>
                             <li>Intento de dolo o fraude. En donde se anulara automáticamente la cobertura del Seguro, comunicando lo acontecido al Contratante de la Póliza, los Sres. Universidad de San Martin de Porres.</li>
