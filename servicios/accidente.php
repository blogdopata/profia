                                 SEGURO DE ACCIDENTES
                              </h5>

                             <b >¿Qué es un accidente?</b>
                             <p class="text-fix-content">
                                

Se considera accidente a toda lesión corporal producida por la acción imprevista, fortuita y/u ocasional de una fuerza externa que actúa de manera súbita o violenta sobre la persona, independientemente de su voluntad y que puede ser determinado por los médicos de una manera cierta.


                             </p>

                             

                             <b>Ámbito de la cobertura</b>
                             <p class="text-fix-content">En accidentes ocurridos dentro de las instalaciones de la facultad y/o cuando el alumno asegurado se encuentre en representación oficial de la universidad, actividades deportivas, que sean únicamente organizadas y autorizadas de manera oficial por las autoridades de la Universidad.</p>

                             

                             <b>Accidentes dentro de los ámbitos de la universidad</b>
                             <p class="text-fix-content">
                          En el caso que el accidente ocurriese dentro de los predios de la facultad, el estudiante, deberá acercarse OBLIGATORIAMENTE a las Unidad médica y/o Tópico dentro del horario de atención indicada en la presente; donde será atendido y evaluado por el servicio médico. En el caso que requiera  la atención en una clínica,  el medico hará entrega de la hoja de Denuncia de accidentes, el cual deberá ser llenado, firmado y sellado por el médico tratante de la Unidad Médica y/o Tópico, indicando el diagnóstico y clínica que pueda ser derivado.
                             </p>



                             <b>Accidentes ocurridos en el cumplimiento de Actividades Deportivas fuera de los predios de la facultad</b>
                             <p class="text-fix-content">
                           Si el estudiante sufriera un accidente al encontrarse realizando actividades oficiales deportivas y/o curriculares organizadas y autorizadas por la facultad, podrá dirigirse a las clínicas afiliadas que constan en la presente cartilla, acompañado del representante de la institución y con la hoja de Denuncia de accidentes, debidamente  llenada y  firmada por representante de la facultad.
                             </p>

